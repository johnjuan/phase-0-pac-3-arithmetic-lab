function add(a, b) {
    return a + b;
}
function subtract(b, a) {
    return b - a;
}
function multiply(a, b) {
    return a * b;
}
function divide(a, b) {
    return a / b;
}
function increment(n) {
    n++;
    return n;
}
function decrement(n) {
    --n;
    return n;
}


